#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;

	if (lst)
	{
		while (lst)
		{
			f(lst);
			lst = lst->next;
		}
	}
}
